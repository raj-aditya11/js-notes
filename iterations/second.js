//while and do while loop 
let index = 0;
while(index <= 10){
    console.log(`value of index is ${index}`);
    index = index + 2;
}

let myArray = ['ironman', 'captain america', 'thor', 'spiderman'];
arr = 0
while(arr < myArray.length){
    console.log(`your superhero is ${myArray[arr]}`);
    arr++;
}

let i = 10;
do{
    console.log(`value is ${i}`);
    i--;
}while(i > 0);

//do while runs at least once as condition is checked after the loop